package com.developercute.moviecatalogue;

public class PemeranModel {
    int photo;
    String pemeran;

    public PemeranModel(int photo, String pemeran) {
        this.photo = photo;
        this.pemeran = pemeran;
    }

    public int getPhoto() {
        return photo;
    }

    public String getPemeran() {
        return pemeran;
    }
}
